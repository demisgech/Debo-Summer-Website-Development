// Comparison operators (<, <=, >, >=, ==,!=, ===,!==)

//Relationa operators (<, <=, >, >=)
console.log(10 > 5);
console.log(10 >= 5);
console.log(10 < 5);
console.log(10 <= 10);

// Equality operators (==,!=, ===,!==)
// lose equality (value)
console.log(10 == 10);
console.log(10 == "10");
console.log(10 != "10");

// strict equality (type + value)
console.log(10 === 10);
console.log(10 === "10");
console.log(10 !== "10");
